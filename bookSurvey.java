    public void setSurveyData(String a, String b, String c){
        final String FILENAME = "UserData/SurveyData.txt";

        FileWriter fw;

        try{
            fw = new FileWriter(FILENAME, true);

            fw.write(a + "," + b + "," + c + "\n");

            fw.close();
        }catch(Exception e){
            e.printStackTrace();
        }
    }    
    
